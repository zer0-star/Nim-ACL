import atcoder/extra/numeric/float128

proc emit32(label: string, bits: uint32) =
  let converted = toFloat128(cast[float32](bits))
  let raw = toBits(converted)
  echo "F32\t", label, "\t",
    toHex(bits, 8), "\t",
    toHex(raw.high, 16), "\t",
    toHex(raw.low, 16)

proc emit64(label: string, bits: uint64) =
